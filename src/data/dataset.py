"""Dataset loading and probe-example construction.

Supports:
  - Synthetic: generated by SyntheticCodeGenerator
  - CodeSearchNet: real Python functions (naturalistic validation set)

Each loader returns a list of ProbeExample objects that carry source code
alongside its ground-truth semantic graph annotations.
"""

from __future__ import annotations

import json
from dataclasses import dataclass, field
from pathlib import Path
from typing import Optional

import jsonlines


@dataclass
class ProbeExample:
    """A single code example with ground-truth semantic annotations."""

    example_id: str
    source: str             # raw Python source code
    language: str = "python"
    label: Optional[int] = None          # task label (e.g., 0=safe, 1=vulnerable)
    metadata: dict = field(default_factory=dict)

    # Populated after graph extraction
    ast_nodes: list = field(default_factory=list)
    dfg_edges: list = field(default_factory=list)
    cfg_nodes: list = field(default_factory=list)
    control_deps: list = field(default_factory=list)

    # Populated after tokenization + activation extraction
    token_ids: list[int] = field(default_factory=list)
    token_strings: list[str] = field(default_factory=list)
    token_offsets: list[tuple[int, int]] = field(default_factory=list)

    def to_dict(self) -> dict:
        return {
            "example_id": self.example_id,
            "source": self.source,
            "language": self.language,
            "label": self.label,
            "metadata": self.metadata,
        }


class CodeProbeDataset:
    """In-memory dataset of ProbeExample objects."""

    def __init__(self, examples: Optional[list[ProbeExample]] = None):
        self.examples: list[ProbeExample] = examples or []

    def __len__(self) -> int:
        return len(self.examples)

    def __getitem__(self, idx: int) -> ProbeExample:
        return self.examples[idx]

    def __iter__(self):
        return iter(self.examples)

    def filter_by_length(self, min_tokens: int = 10, max_tokens: int = 2048) -> "CodeProbeDataset":
        kept = [e for e in self.examples
                if min_tokens <= len(e.token_ids) <= max_tokens]
        return CodeProbeDataset(kept)

    def split(self, train_frac: float = 0.8, seed: int = 42) -> tuple["CodeProbeDataset", "CodeProbeDataset"]:
        import random
        rng = random.Random(seed)
        indices = list(range(len(self.examples)))
        rng.shuffle(indices)
        split_at = int(len(indices) * train_frac)
        train_ex = [self.examples[i] for i in indices[:split_at]]
        test_ex = [self.examples[i] for i in indices[split_at:]]
        return CodeProbeDataset(train_ex), CodeProbeDataset(test_ex)

    def save(self, path: str | Path):
        path = Path(path)
        path.parent.mkdir(parents=True, exist_ok=True)
        with jsonlines.open(path, mode="w") as writer:
            for ex in self.examples:
                writer.write(ex.to_dict())

    @classmethod
    def load(cls, path: str | Path) -> "CodeProbeDataset":
        examples = []
        with jsonlines.open(path) as reader:
            for i, obj in enumerate(reader):
                ex = ProbeExample(
                    example_id=obj.get("example_id", str(i)),
                    source=obj["source"],
                    language=obj.get("language", "python"),
                    label=obj.get("label"),
                    metadata=obj.get("metadata", {}),
                )
                examples.append(ex)
        return cls(examples)


def load_jsonl(path: str | Path) -> list[dict]:
    with jsonlines.open(path) as reader:
        return list(reader)


def save_jsonl(data: list, path: str | Path):
    path = Path(path)
    path.parent.mkdir(parents=True, exist_ok=True)
    with jsonlines.open(path, mode="w") as writer:
        for obj in data:
            writer.write(obj.to_dict() if hasattr(obj, "to_dict") else obj)


def load_codesearchnet_sample(
    n: int = 200,
    split: str = "validation",
    min_chars: int = 100,
    max_chars: int = 2000,
    seed: int = 42,
) -> CodeProbeDataset:
    """Sample real Python functions from CodeSearchNet for the naturalistic
    validation set (E8).

    Filters to functions that parse with `ast` (required for ground-truth
    graph extraction) and fall within a length band, then samples n with a
    fixed seed. Requires network access on first call (HF datasets cache
    afterwards) — run locally and commit/rsync the resulting jsonl if the
    cluster has no internet access.
    """
    import ast as _ast
    import random as _random

    try:
        from datasets import load_dataset
        ds = load_dataset("code-search-net/code_search_net", "python", split=split,
                          trust_remote_code=True)
    except Exception as e:
        raise RuntimeError(
            f"Failed to load CodeSearchNet: {e}. "
            "Check network access and `pip install datasets`."
        ) from e

    candidates = []
    for i, row in enumerate(ds):
        source = row["whole_func_string"]
        if not (min_chars <= len(source) <= max_chars):
            continue
        try:
            _ast.parse(source)
        except (SyntaxError, ValueError):
            continue
        candidates.append((i, source, row.get("func_documentation_string", "")))
        if len(candidates) >= n * 20:   # enough pool to sample from
            break

    rng = _random.Random(seed)
    rng.shuffle(candidates)
    examples = [
        ProbeExample(
            example_id=f"csn_{i}",
            source=source,
            metadata={"type": "real", "docstring": doc[:200]},
        )
        for i, source, doc in candidates[:n]
    ]
    return CodeProbeDataset(examples)
